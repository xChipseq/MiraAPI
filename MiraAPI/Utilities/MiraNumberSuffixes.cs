/// </summary>
public enum MiraNumberSuffixes
{
    /// <summary>
    /// No suffix.
    /// </summary>
    None,

    /// <summary>
    /// Multiplier suffix (x).
    /// </summary>
    Multiplier,

    /// <summary>
    /// Seconds suffix (s).
    /// </summary>
    Seconds,

    /// <summary>
    /// Percentage suffix (%).
    /// </summary>
    Percent,
}
